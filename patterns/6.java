import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    int n=5;
	    for(int i=n;i>0;i--){
	        for(int j=0;j<i;j++){
	            System.out.print(j+1);
	        }
	        System.out.println();
	    }

	}
}
